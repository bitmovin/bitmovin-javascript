/**
 * The operator that should be used for the evaluation
 * @export
 * @enum {string}
 */
enum ConditionOperator {
    EQUAL = '==',
    NOT_EQUAL = '!=',
    LESS_THAN_OR_EQUAL = '<=',
    LESS_THAN = '<',
    GREATER_THAN = '>',
    GREATER_THAN_OR_EQUAL = '>='
}

export default ConditionOperator;

